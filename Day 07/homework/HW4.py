# 4)მიგეცემათ რიცხვების სია, რომელშიც არის უარყოფითი რიცხვებიც.
# დაწერეთ ფუნქცია, რომელიც აბრუნებს ახალ სიას, სადაც მხოლოდ
# დადებითი და ნულის ტოლი რიცხვებია. მაგ:
#  შეყვანა: [-5, 3, -1, 0, 7] გამოტანა: [3, 0, 7]

def negativ_lst(lst):
    result = []
    for x in lst:
        if x>=0:
            result.append(x)
    return result
print(negativ_lst([-5, 3, -1, 0, 7]))