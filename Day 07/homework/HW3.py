# 3)მიგეცემათ სია, რომელშიც ზოგი რიცხვი იმეორებს თავს.
# თქვენი მიზანია დაწეროთ ფუნქცია, რომელიც აბრუნებს სიას მხოლოდ უნიკალური
# რიცხვებით (გაუმეორებლად).შეყვანა მაგ: [1, 2, 2, 3, 4, 4, 5]
#  გამოტანა: [1, 2, 3, 4, 5]


def repeat_lst(lst):
    result = []
    for i in lst:
        if i not in result:
            result.append(i)
    return result
print(repeat_lst([1, 2, 2, 3, 4, 4, 5]))